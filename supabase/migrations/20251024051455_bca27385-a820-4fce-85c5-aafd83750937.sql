-- Fix security warnings for function search paths

-- Update saved_lyrics trigger function
CREATE OR REPLACE FUNCTION update_saved_lyrics_updated_at()
RETURNS TRIGGER 
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$;

